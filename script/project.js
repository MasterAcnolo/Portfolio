// RENDER FEATURED
function renderFeatured() {
    const p = featuredProject;
    const hasProject = p.projectLink;
    
    document.getElementById('featuredContainer').innerHTML = `
        <div class="featured-card">
            <div class="featured-content">
                <img src="${p.image}" alt="${p.title}" class="featured-image" onerror="this.style.background='linear-gradient(135deg, #667eea 0%, #764ba2 100%)'; this.src='';">
                <div class="featured-body">
                    <span class="featured-badge">Projet Principal</span>
                    <h2 class="featured-title">${p.title}</h2>
                    <p class="featured-description">${p.description}</p>
                    <div class="featured-tags">
                        ${p.tags.map(tag => `<span class="featured-tag">${tag}</span>`).join('')}
                    </div>
                    <div class="featured-footer">
                        ${hasProject ? `<button class="featured-btn featured-btn-primary" onclick="window.open('${p.projectLink}', '_blank')">Voir le site</button>` : ''}
                        <button class="featured-btn ${hasProject ? 'featured-btn-secondary' : 'featured-btn-primary'}" onclick="window.open('${p.githubLink}', '_blank')">Voir sur GitHub</button>
                    </div>
                </div>
            </div>
        </div>
    `;
}

